package JAVA8_CONVERSION_PROGRAMS;

public class charToInt {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'B';

        int valueCh1 = ch1;
        int valueCh2 = ch2;

        System.out.println("ASCII value of " + ch1 + " : " + valueCh1);
        System.out.println("ASCII value of " + ch2 + " : " + valueCh2);
    }
}
